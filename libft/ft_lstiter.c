#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*current;

	if (!lst || !f)
		return ;
	while (lst)
	{
		current = lst->next;
		f(lst->content);
		lst = current;
	}
}
